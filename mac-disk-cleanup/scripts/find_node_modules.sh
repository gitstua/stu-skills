#!/usr/bin/env bash
# Find all node_modules outside managed dirs, with size and parent last-modified date
# Usage: find_node_modules.sh [search_root]
# Output: tab-separated: size_mb  date  path

ROOT="${1:-$HOME}"

find "$ROOT" \
  -name "node_modules" \
  -type d \
  -maxdepth 8 \
  -not -path '*/.nvm/*' \
  -not -path '*/node_modules/*/node_modules' \
  -not -path '*/.vscode/extensions/*' \
  -not -path '*/.copilot/*' \
  -not -path '*/.lmstudio/*' \
  -not -path '*/.cache/opencode/*' \
  -not -path '*/.local/share/opencode/*' \
  -not -path '*/.config/opencode/*' \
  -not -path '*/Library/Application Support/Claude/*' \
  -not -path '*/Library/Caches/*' \
  2>/dev/null \
| while read -r dir; do
  size=$(du -sm "$dir" 2>/dev/null | cut -f1)
  parent=$(dirname "$dir")
  modified=$(stat -f "%Sm" -t "%Y-%m-%d" "$parent" 2>/dev/null)
  printf "%s\t%s\t%s\n" "$size" "$modified" "$dir"
done | sort -k2
